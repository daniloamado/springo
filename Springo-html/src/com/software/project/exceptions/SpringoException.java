package com.software.project.exceptions;

public class SpringoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SpringoException() {

	}

	public SpringoException(String arg0) {
		super(arg0);

	}

	public SpringoException(Throwable arg0) {
		super(arg0);

	}

	public SpringoException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

}